import UIKit

//var é usado para variáveis que alteram o valor
var bemVindo = "Hello, playground"

//let é uma constante que tem o valor único onde não pode ser alterado
let myDogName = "Bento"

//Exemplo sem declaração de tipo
let inteiro = 1 //Int = Inteiro
let double = 3.14 // Double  = números decimais
let string = "Aqui vem um texto" // String = texto
let bool = true //bool = boleano (true ou false)

//Exemplo com declaração de tipo
let int:Int = 4
let pi:Double = 3.14
let name:String = "Meu nome é Edson"
let no:Bool = false



